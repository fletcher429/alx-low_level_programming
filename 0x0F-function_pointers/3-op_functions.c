#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

/**
 * op_add - adds two integers
 * @a: first int
 * @b: second int
 * Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substarct two integers
 * @a: first int
 * @b: second int
 * Return: substraction
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - adds two integers
 * @a: first int
 * @b: second int
 * Return: multiplication
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: first int
 * @b: second int
 * Return: division
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module two integers
 * @a: first int
 * @b: second int
 * Return: modulo
*/

int op_mod(int a, int b)
{
	return (a % b);
}

